int main(void){
	TExaS_Init(SSI0_Real_Nokia5110_Scope);
	PortF_Init();
  Random_Init(1);
  Nokia5110_Init();
  Nokia5110_ClearBuffer();
	Nokia5110_DisplayBuffer();
	SW1 = GPIO_PORTF_DATA_R&(1<<4);
  SW2 = GPIO_PORTF_DATA_R&(1<<0);
	welcome();
  while(1){
		SW1 = GPIO_PORTF_DATA_R&(1<<4);     // read PF4 into SW1
    SW2 = GPIO_PORTF_DATA_R&(1<<0);     // read PF0 into SW2
		if(!(enemies_speed--)){
			move_enemies();
			
			player_shot();
			enemies_speed = 3;
		}
		
		if(! (enemy_shot_speed--)){
			if(enemy_missile.state == 0)
				enemy_shot();
			
			move_enemy_shot();
			enemy_shot_speed = 1;
		}
		if(!SW1)
			move_player_right();
		else if(!SW2)
			move_player_left();
    draw();
    Delay50ms(1);
		
  }

}

void Delay50ms(unsigned long count){unsigned long volatile time;
  while(count>0){
    time = 363620;  
    while(time){
	  	time--;
    }
    count--;
  }
}

void Missile_delay(unsigned long count){unsigned long volatile time;
  while(count>0){
    time = 9090;  
    while(time){
	  	time--;
    }
    count--;
  }
}

void PortF_Init(void){ volatile unsigned long delay;
  SYSCTL_RCGC2_R |= 0x00000020;     // 1) F clock
  delay = SYSCTL_RCGC2_R;           // delay   
  GPIO_PORTF_LOCK_R = 0x4C4F434B;   // 2) unlock PortF PF0  
  GPIO_PORTF_CR_R = 0x1F;           // allow changes to PF4-0       
  GPIO_PORTF_AMSEL_R = 0x00;        // 3) disable analog function
  GPIO_PORTF_PCTL_R = 0x00000000;   // 4) GPIO clear bit PCTL  
  GPIO_PORTF_DIR_R = 0x0E;          // 5) PF4,PF0 input, PF3,PF2,PF1 output   
  GPIO_PORTF_AFSEL_R = 0x00;        // 6) no alternate function
  GPIO_PORTF_PUR_R = 0x11;          // enable pullup resistors on PF4,PF0       
  GPIO_PORTF_DEN_R = 0x1F;          // 7) enable digital pins PF4-PF0        
}

void init(){
	int i;
	for(i=0;i<5;i++){
    Enemy[i].x = 16*i;
    Enemy[i].y = 10;
    Enemy[i].image = SmallEnemy30PointA;
    Enemy[i].health = 1;
  }
	
	
	Player.x = 32;
	Player.y = 47;
	Player.image = PlayerShip0;
	Player.health = 1;
	
	Bunker.x = 33;
	Bunker.y = 47 - PLAYERH;
	Bunker.image = Bunker0;
	Bunker.health = 3;
	
	no_of_enemies = 5;
}

void move_enemies(void){
	int i;
  for(i=0;i<5;i++){
    if(move_enemies_right)
      Enemy[i].x += 2; // move to right
		else
      Enemy[i].x -= 2; // move to left
  }
	
	if(Enemy[4].x + ENEMY30W >= 82)
				move_enemies_right = 0;
	else if(Enemy[0].x <=2)
		move_enemies_right = 1;
}


void move_player_right(){
	if(Player.x + PLAYERW <= 82)
		Player.x += 2;
}

void move_player_left(){
	if(Player.x >= 2)
		Player.x -= 2;
}

void draw(void){
	int i;
  Nokia5110_ClearBuffer();
  for(i=0;i<5;i++){
    if(Enemy[i].health > 0){
			Nokia5110_PrintBMP(Enemy[i].x, Enemy[i].y, Enemy[i].image, 0);
    }
  }
	Nokia5110_PrintBMP(Player.x, Player.y, Player.image, 0);
	Nokia5110_PrintBMP(Bunker.x, Bunker.y, Bunker.image, 0);
	if(Player_missile.state)
		Nokia5110_PrintBMP(Player_missile.x, Player_missile.y, Player_missile.image, 0);
	
	if(enemy_missile.state)
		Nokia5110_PrintBMP(enemy_missile.x, enemy_missile.y, enemy_missile.image, 0);
		
	
  Nokia5110_DisplayBuffer();      // draw buffer
}

void player_shot(){
	int i;
	Player_missile.x = Player.x + PLAYERW/2;
	Player_missile.y = Player.y + 10;
	Player_missile.image = Laser0;
	Player_missile.state = 1;
	while(Player_missile.y >= 0){
		Player_missile.y -= 2;
	
	
		for(i=0; i<5; i++){
			if(Player_missile.x >= Enemy[i].x && Player_missile.x <= Enemy[i].x + ENEMY30W && Player_missile.y >= Enemy[i].y && Player_missile.y <= Enemy[i].y + ENEMY30H){
				Enemy[i].image = SmallExplosion0;
				draw();
				Delay50ms(1);
				Player_missile.state = 0;
				Enemy[i].health = 0;
				return;
			}	
		}
		draw();
		Missile_delay(1);
	}
	Player_missile.state = 0;
}

void sw_to_start(){
	Nokia5110_Clear();
  Nokia5110_SetCursor(1, 1);
  Nokia5110_OutString("PRESS ANY");
  Nokia5110_SetCursor(1, 2);
  Nokia5110_OutString("SWITCH TO");
	Nokia5110_SetCursor(1, 3);
  Nokia5110_OutString("START");
	while(SW1 && SW2){
		SW1 = GPIO_PORTF_DATA_R&(1<<4);
    SW2 = GPIO_PORTF_DATA_R&(1<<0);
	}
	init();
	draw();
}

void game_over(){
	Nokia5110_Clear();
  Nokia5110_SetCursor(1, 1);
  Nokia5110_OutString("GAME OVER");
	Delay50ms(5);
	sw_to_start();
}

